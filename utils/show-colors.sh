#!/bin/sh
#
# Show all the color combinations


NORMAL="\033[30;00m" 
BOLD="\033[30;01m"
UNDERSCORE="\033[30;04m"
BLINK="\033[30;05m"
REVERSE="\033[30;07m"
CONCEALED="\033[30;08m"

FG_WHITE='\033[1;37m'
FG_LIGHTGRAY='\033[0;37m'
FG_GRAY='\033[1;30m'
FG_BLACK='\033[0;30m'
FG_RED='\033[0;31m'
FG_LIGHTRED='\033[1;31m'
FG_GREEN='\033[0;32m'
FG_LIGHTGREEN='\033[1;32m'
FG_BROWN='\033[0;33m'
FG_YELLOW='\033[1;33m'
FG_BLUE='\033[0;34m'
FG_LIGHTBLUE='\033[1;34m'
FG_PURPLE='\033[0;35m'
FG_PINK='\033[1;35m'
FG_CYAN='\033[0;36m'
FG_LIGHTCYAN='\033[1;36m'

BG_GREY="\033[40m"
BG_RED="\033[41m"
BG_GREEN="\033[42m"
BG_YELLOW="\033[43m"
BG_BLUE="\033[44m"
BG_MAGENTA="\033[45m"
BG_CYAN="\033[46m"
BG_WHITE="\033[47m"
BG_DEFAULT="\033[0;49m"

for style in ${NORMAL} ${BOLD} ${UNDERSCORE} ${BLINK} ${REVERSE} ${CONCEALED} ; do
    for fg in ${FG_WHITE} ${FG_LIGHTGRAY} ${FG_GRAY} ${FG_BLACK} ${FG_RED} ${FG_LIGHTRED} ${FG_GREEN} ${FG_LIGHTGREEN} ${FG_BROWN} ${FG_YELLOW} ${FG_BLUE} ${FG_LIGHTBLUE} ${FG_PURPLE} ${FG_PINK} ${FG_CYAN} ${FG_LIGHTCYAN} ; do
	for bg in ${BG_GREY} ${BG_RED} ${BG_GREEN} ${BG_YELLOW} ${BG_BLUE} ${BG_MAGENTA} ${BG_CYAN} ${BG_WHITE} ${BG_DEFAULT} ; do
	    echo -e ${style}${fg}${bg}Hello World!${NORMAL}
        done
    done
done
